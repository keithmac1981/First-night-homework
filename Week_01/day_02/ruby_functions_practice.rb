    string_one = 'Mary had a little lamb, '
    string_two = 'its fleece was white as snow'
    return string_one + string_two
end

def add_string_as_number(string_one, string_two)
    string_one = 1
    string_two = 2
    return string_one.to_i + string_two.to_i
end


